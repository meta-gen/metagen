/**
 * ÌëúÏ§ÄÏö©Ïñ¥Î•º Îì±Î°ùÌïúÎã§.
 */
function addStandardTerms(){
    const columnList = window.tableInstances["standardTerms"]
        .settings()
        .init()
        .columns
        .filter(col => col.data !== "id" && col.data !== "isApprovalYn")
        .map(col => {
            return {
                column: col.data,
                columnName: col.title || col.columnName || col.data
            };
        });

    const dialog = $("#mainConfirm")[0];
    const dialogTitle = $("#mainDialogTitle");
    const dialogContent = $("#mainDialogContent");

    dialogTitle.text("ÌëúÏ§ÄÏö©Ïñ¥ Ï∂îÍ∞Ä");
    dialogContent.empty(); // Ï¥àÍ∏∞Ìôî

    const form = $("<form></form>").attr("id", "add-domain-form");

    columnList.forEach(col => {
        const key = col.column;
        const labelText = col.columnName || key;

        if (key === "commonStandardTermAbbreviation") {
            const label = $("<label></label>")
                .attr("for", key)
                .text(labelText)
                .css({
                    display: "block",
                    marginTop: "10px",
                    fontWeight: "bold"
                });

            const wrapper = $("<div></div>").css({
                display: "flex",
                alignItems: "center",
                gap: "10px",
                marginBottom: "10px"
            });

            const input = $("<input>")
                .attr("type", "text")
                .attr("id", key)
                .attr("name", key)
                .prop("readonly", true)
                .css({
                    flex: "1",
                    backgroundColor: "#f0f0f0",
                    height: "38px",
                    paddingLeft: "10px",
                    border: "1px solid #ccc",
                    borderRadius: "4px"
                });

            const searchBtn = $("<button></button>")
                .attr("type", "button")
                .addClass("btn btn-secondary")
                .html("üîç")
                .css({
                    height: "38px"
                })
                .on("click", function () {
                    openDomainSearchPopup();
                });

            wrapper.append(input, searchBtn);
            form.append(label, wrapper); // ‚úÖ label Îî∞Î°ú, wrapper Îî∞Î°ú append
        }else{

            const label = $("<label></label>")
                .text(labelText)
                .attr("for", key)
                .css("display", "block")
                .css("margin-top", "10px");

            let input;

            if (["commonStandardDomainDescription", "allowedValues"].includes(key)) {
                input = $("<textarea></textarea>")
                    .attr("id", key)
                    .attr("name", key)
                    .css("width", "100%")
                    .css("height", "60px");
            } else {
                input = $("<input>")
                    .attr("type", "text")
                    .attr("id", key)
                    .attr("name", key)
                    .css("width", "100%");
            }

            // id, projectIdÎäî Ï†úÏô∏ÌïòÍ±∞ÎÇò readonly
            if (key === "id" || key === "projectId") {
                input.prop("readonly", true).css("background-color", "#f0f0f0");
            }

            form.append(label, input);
        }
    });

    const saveBtn = $("<button></button>")
        .attr("type", "submit")
        .text("Ï∂îÍ∞Ä")
        .addClass("btn btn-primary")
        .css("margin-top", "20px");

    form.append(saveBtn);
    dialogContent.append(form);

    dialog.showModal();
}

